public class LeftShiftOperator {
    public static void main(String[] args) {
        int x = 3;
        System.out.println(x<<1);
        System.out.println(x<<2);
        int y = 4;
        System.out.println(x<<y);
        int z = -1;
        System.out.println(z<<1);
        System.out.println(z<<2);
    }
    
}
